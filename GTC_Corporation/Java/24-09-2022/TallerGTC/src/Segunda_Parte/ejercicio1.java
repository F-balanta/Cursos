package Segunda_Parte;

public class ejercicio1 {
    public static void main(String[] args) {
        operaciones();
    }

    public static void operaciones()
    {
        int x = 144, y = 999;
        System.out.println("Suma: \n\t\t" + x +" + " + y + " = " + (x + y));
        System.out.println("Resta: \n\t\t" + x +" - " + y + " = " + (x - y));
        System.out.println("Multiplicación: \n\t\t" + x +" * " + y + " = " + (x * y));
        System.out.println("División: \n\t\t" + x +" / " + y + " = " + (x / y));
    }
}
