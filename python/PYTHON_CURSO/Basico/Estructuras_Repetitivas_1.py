"""
Estructuras_Repetitivas_1.py

Script en python que muestre los numeros del 0 al 13 utilizando
la estructura repetitiva "while"
"""

print("This program display numbers of 0 at 13")
numero = 0

while numero <= 13:
    print(f"Contador = {numero} ")
    numero += 1
else:
    print("He terminado de contar")