"""
Script en python que calcule el are√° de un triangulo.
El usuario debera ingresar tanto el valor de la base como el de la altura.

"""

print("********** CALCULEMOS EL AREA DE UN TRIANGULO**********")
base = int(input("Digita la base: "))
print("Ahora...")
altura = int(input("Digita la altura: "))

area = (base * altura) / 2

print(f"EL AREA DEL TRIANGULO ES: {area}")